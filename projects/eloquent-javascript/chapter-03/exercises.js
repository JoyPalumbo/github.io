// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  if(num1 < num2){
  return num1;
  }
  if(num2 < num1){
  return num2;
  }
  if(num2 === num1){
    return 0;
  }
  
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
if(num === 0) {
  return true;
}
else if(num === 1) {
  return false;
}
else if(num < -1) {
  return isEven(-num);
}
else {
  return isEven(num - 2);
}
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
 var bStr = 0;
  for(var i = 0; i < str.length; i++){
    if(str[i] === char) {
      bStr ++;
    }
  }
    return bStr;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  var bStr = 0;
  for(var i = 0; i < str.length; i++){
    if(str[i] === 'B') {
      bStr ++;
    }
  }
    return bStr;
}

