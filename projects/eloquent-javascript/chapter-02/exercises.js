function drawChessboard(num) {
   let str = "";
  for(var y = 0; y < num; y++){
    for(var x = 0; x < num; x++){
      if((x+y) % 2 == 0) {
        str += " ";
      }
      else {
        str += "#";
      }
    }
    str += "\n";
  }

  console.log(str);

}
